package calc

func Addition(num1, num2 int) int {
	result := num1 + num2
	return result
}

func Substraction(num1, num2 int) int {
	result := num1 - num2
	return result
}

func Multiplication(num1, num2 int) int {
	result := num1 * num2
	return result

}
func Division(num1, num2 int) int {
	result := num1 / num2
	return result

}
